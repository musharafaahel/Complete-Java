package basics;

public class OperatorDemo1 {

	public static void main(String[] args) {
		int x = 13;
		int y = 12;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);
		
	}

}
